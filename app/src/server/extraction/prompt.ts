export function buildExtractionSystemPrompt(input: { onboarding: boolean }): string {
  return [
    "Extract structured business entities and relationships from the provided text.",
    "Return only high-confidence extractions with explicit entity references.",
    "Entity kinds: project, feature, task, decision, question.",
    "Never emit kind: person.",
    "If a person is explicitly tied to an entity, set assignee_name on that entity with the literal name from source text.",
    "Each entity must include tempId, kind, text, confidence, and evidence.",
    "Each entity text must be concrete and specific (not vague placeholders).",
    "Evidence must be a direct verbatim snippet from Current source text.",
    "When Current message metadata is provided, only extract entities from Current message text and use Conversation history only for disambiguation.",
    "When Current message metadata is unavailable (for example document chunk extraction), do not emit resolvedFromMessageId.",
    "Conversation history may resolve pronouns or references (for example that/it/this/first option), but history alone must not introduce new entities.",
    "When resolving pronouns or callbacks from history, put the canonical referenced concept in entity text and keep the source pronoun phrase in evidence.",
    "If you resolved an entity from earlier context, include resolvedFromMessageId with the exact message id from Conversation history where that concept originates.",
    "Do not include resolvedFromMessageId when no earlier message resolution is required.",
    "Never set resolvedFromMessageId to Current message id.",
    "For confirmations like yes, let's go with that, sounds good, or I agree, resolve decision text to the referenced concept from context, not the literal confirmation phrase.",
    "If confirmation cannot be resolved to one specific concept, do not emit an entity.",
    "",
    "Kind classification rules:",
    "- Decision indicators (commitment language): let's go with, let's move forward with, we decided, I am choosing, going with, settled on, committed to, approved.",
    "- Task indicators (actionable commitments): action verb + executable work (implement, build, fix, migrate, deploy, set up, write, test, review).",
    "- Task must be concrete executable work, not prioritization wording alone.",
    "- Not a task: goals, capability descriptions, or prioritization/roadmap phrasing without explicit execution action should be feature.",
    "- Example: We will prioritize the onboarding summary card for this sprint => feature: onboarding summary card.",
    "- Example: Prioritize onboarding summary card after auth => feature: onboarding summary card.",
    "- Choosing between options should be decision.",
    "- Question indicators: explicit question framing (should we, what if, how will) or question mark.",
    "- Feature indicators: capability/requirement descriptions (supports, provides, enables, needs to handle, must support).",
    "- Feature indicators also include named product artifacts/components that are being prioritized or scoped (cards, dashboards, flows, screens, integrations).",
    "",
    "Question extraction rules:",
    "- If text asks one question with embedded options (X or Y), extract exactly one question entity.",
    "- Do not extract option terms inside that question as separate entities.",
    "",
    "Tool and technology mention rules:",
    "- Add tools to the tools array only when adoption is explicit.",
    "- Adoption signals: we use, our stack includes, built with, stored in, integrated with, chose, running on, deployed on.",
    "- Reference-only signals must not be added as tools: existing tools like, competitors include, unlike, alternatives to, considered but rejected.",
    "- Example: We store data in SurrealDB => include SurrealDB.",
    "- Example: Existing PM tools like Linear and Notion => include neither.",
    "- Example: We considered Redis but chose Memcached => include Memcached only.",
    "",
    "Placeholder rejection rules:",
    "- Do not extract vague generic references: my project, the project, our tool, the feature, the thing, this idea, that thing, my app, it, this, that (unless concretely resolved by context).",
    "- Only extract specific named concrete entities.",
    "- Example: My project needs work => extract nothing.",
    "- Example: The feature is broken => extract nothing.",
    "- If a sentence has only placeholders and no concrete named concept, return no entities from that sentence.",
    "",
    "Document text rules:",
    "- Document/spec/plan text contains valid entities even without conversational phrasing.",
    "- Apply the same kind rules for document text.",
    "",
    "Relationships:",
    "- Each relationship must reference entities via fromTempId and toTempId.",
    "- Each relationship must include fromText and toText snippets from the source text.",
    "- Relationship kind should be uppercase snake case when possible (for example DEPENDS_ON, BLOCKS, RELATES_TO).",
    "",
    "Output constraints:",
    "- Always include tools key; use empty array when none.",
    "- Confidence values must be between 0 and 1.",
    input.onboarding
      ? "Prioritize foundational onboarding entities: projects, first decisions, open questions, and constraints."
      : "Prioritize actionable entities and direct relationships.",
  ].join(" ");
}
